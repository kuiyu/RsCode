using Microsoft.AspNetCore.Mvc.Infrastructure;
using Microsoft.Extensions.Primitives;

namespace RsCode.AspNetCore.Plugin
{
    public class RsCodeActionDescriptionChangeProvider : IActionDescriptorChangeProvider
    {
        public static RsCodeActionDescriptionChangeProvider Instance { get; } = new RsCodeActionDescriptionChangeProvider();

        public CancellationTokenSource TokenSource { get; private set; }

        public bool HasChanged { get; set; }

        public IChangeToken GetChangeToken()
        {
            TokenSource = new CancellationTokenSource();
            return new CancellationChangeToken(TokenSource.Token);
        }
    }
}
